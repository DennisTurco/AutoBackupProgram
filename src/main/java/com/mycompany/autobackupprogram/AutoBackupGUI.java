    public static int countFilesInDirectory(File directory) {
    	int count = 0;
    	
    	for (File file : directory.listFiles()) {
            if (file.isFile()) {
                    count++;
            }

            if (file.isDirectory()) {
                    count += countFilesInDirectory(file);
            }
    	} 	
    	return count;
    }
    
